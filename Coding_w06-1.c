#include <stdio.h> // นำเข้าไลบรารี Standard Input/Output Header (stdio.h) เพื่อใช้งานฟังก์ชันที่เกี่ยวข้องกับการรับข้อมูลและแสดงผล เช่น printf()

int main() {
    int a = 5, b = 17;       // กำหนดตัวแปรชนิดจำนวนเต็ม (int) ชื่อ a และ b โดยให้ a มีค่าเริ่มต้นเป็น 5 และ b มีค่าเริ่มต้นเป็น 17
    float c = 8.5, d = 4.0;  // กำหนดตัวแปรชนิดทศนิยม (float) ชื่อ c และ d โดยให้ c มีค่าเริ่มต้นเป็น 8.5 และ d มีค่าเริ่มต้นเป็น 4.0

    printf("บรรทัดที่ 1 : a = %d, b = %d, c = %.1f, d = %.1f\n", a, b, c, d); // แสดงค่าของตัวแปรทั้งหมดที่กำหนดไว้ โดยใช้ %d สำหรับจำนวนเต็มและ %.1f สำหรับทศนิยม 1 ตำแหน่ง

    printf("บรรทัดที่ 2 : d + a = %.1f\n", d + a); // ผลการคำนวณของ d + a (4.0 + 5) ผลลัพธ์จะเป็นทศนิยม (float)
    printf("บรรทัดที่ 3 : a - b = %d\n", a - b);   // ผลการคำนวณของ a - b (5 - 17) ผลลัพธ์จะเป็นจำนวนเต็ม (int)
    printf("บรรทัดที่ 4 : c * d = %.2f\n", c * d); // ผลการคำนวณของ c * d (8.5 * 4.0) ผลลัพธ์จะเป็นทศนิยม (float)
    printf("บรรทัดที่ 5 : a * c = %.2f\n", a * c); // ผลการคำนวณของ a * c (5 * 8.5) ผลลัพธ์จะเป็นทศนิยม (float)
    printf("บรรทัดที่ 6 : c / d = %.2f\n", c / d); // ผลการคำนวณของ c / d (8.5 / 4.0) ผลลัพธ์จะเป็นทศนิยม (float)
    printf("บรรทัดที่ 7 : b / c = %.2f\n", b / c); // ผลการคำนวณของ b / c (17 / 8.5) ผลลัพธ์จะเป็นทศนิยม (float)
    printf("บรรทัดที่ 8 : a %% b = %d\n", a % b);   // ผลการคำนวณของ a % b (5 % 17) ซึ่งเป็นการหาเศษจากการหาร ผลลัพธ์คือ 5 (ใช้ได้กับ int เท่านั้น)
    printf("บรรทัดที่ 9 : (int)c %% a = %d\n", ((int)c) % a); // ผลการคำนวณของ c % a โดยต้องแปลงค่า c (8.5) เป็นจำนวนเต็มก่อน (ได้ 8) แล้วจึงหาเศษจากการหารด้วย a (8 % 5 = 3)
    printf("บรรทัดที่ 10 : (int)c %% (int)d = %d\n", ((int)c) % ((int)d)); // ผลการคำนวณของ c % d โดยต้องแปลงทั้ง c (8.5 -> 8) และ d (4.0 -> 4) เป็นจำนวนเต็มก่อน แล้วจึงหาเศษจากการหาร (8 % 4 = 0)

    return 0; // คืนค่า 0 เพื่อบอกระบบปฏิบัติการว่าโปรแกรมทำงานเสร็จสิ้นโดยไม่มีข้อผิดพลาด
}