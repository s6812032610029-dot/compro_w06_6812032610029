#include <stdio.h> // บรรทัดนี้เป็นการเรียกใช้งานไลบรารี Standard Input/Output Header (stdio.h) ซึ่งเป็นไลบรารีมาตรฐานที่จำเป็นสำหรับการใช้ฟังก์ชัน input/output อย่าง printf()

#include <stdio.h>  // นำเข้าไลบรารีมาตรฐาน <stdio.h> สำหรับใช้งานฟังก์ชันรับค่า (input) และแสดงผล (output)

int main() {  // ฟังก์ชันหลักของโปรแกรม เป็นจุดเริ่มต้นของการทำงานเมื่อรันโปรแกรม

    // ───────────────────────────────────────────────
    // ตอนที่ 1: การดำเนินการทางคณิตศาสตร์ด้วยตัวแปรประเภทจำนวนเต็ม (int)
    // ───────────────────────────────────────────────
    int i = 1, j = 2, k;  // ประกาศและกำหนดค่าเริ่มต้นให้ตัวแปร i และ j โดยที่ i = 1 และ j = 2 ส่วน k ยังไม่มีค่า

    printf("ตอนที่ 1:\n");

        // แสดงค่าของตัวแปร i และ j ที่ประกาศไว้ตอนต้น
        printf("บรรทัดที่ 1: กำหนดค่าเริ่มต้น → i = %d, j = %d\n", i, j);

        // คำนวณผลรวมของ i และ j แล้วนำไปเก็บไว้ในตัวแปร k
        k = i + j;
        printf("บรรทัดที่ 2: k = i + j = %d + %d = %d\n", i, j, k);

        // นำค่าของ k คูณกับ j แล้วบวกกับ i เดิม จากนั้นเก็บค่าใหม่กลับลงใน i
        // เป็นการเปลี่ยนแปลงค่าของ i ให้มีค่าใหม่จากสูตร: i = i + (k * j)
        i = i + (k * j);
        printf("บรรทัดที่ 3: i = i + (k * j) = %d + (%d * %d) = %d\n", i - (k * j), k, j, i);

        // นำค่าปัจจุบันของ i มาแบ่งด้วย 2 แล้วเก็บผลลัพธ์ไว้ใน j (การหารจำนวนเต็มจะปัดเศษทิ้ง)
        j = i / 2;
        printf("บรรทัดที่ 4: j = i / 2 = %d / 2 = %d\n", i, j);

        // หารค่า j ด้วย 2 แล้วเก็บ "เศษ" จากการหารไว้ในตัวแปร k (ใช้เครื่องหมาย % เรียกว่า modulo)
        k = j % 2;
        printf("บรรทัดที่ 5: k = j %% 2 = %d %% 2 = %d\n", j, k);

        // นำค่าของ j และ k มาบวกกัน แล้วคูณด้วย 3 จากนั้นเก็บค่าผลลัพธ์กลับลงใน i
        i = (j + k) * 3;
        printf("บรรทัดที่ 6: i = (j + k) * 3 = (%d + %d) * 3 = %d\n", j, k, i);

        // แสดงค่าปัจจุบันของตัวแปรทั้งสาม i, j และ k หลังจากกระบวนการทั้งหมดในตอนที่ 1
        printf("บรรทัดที่ 7: ผลลัพธ์สุดท้าย → i = %d, j = %d, k = %d\n\n", i, j, k);


    // ───────────────────────────────────────────────
    // ตอนที่ 2: การดำเนินการทางคณิตศาสตร์ด้วยตัวแปรประเภททศนิยม (double)
    // ───────────────────────────────────────────────
    double x = 1.0, y = 2.0, z;  // ประกาศตัวแปรทศนิยม โดยกำหนดให้ x = 1.0, y = 2.0 ส่วน z ยังไม่มีค่า

    printf("ตอนที่ 2:\n");

        // แสดงค่าของ x และ y ที่กำหนดไว้ตอนต้น
        printf("บรรทัดที่ 1: กำหนดค่าเริ่มต้น → x = %.1f, y = %.1f\n", x, y);

        // คำนวณ x = y + 5.0 แล้วเก็บผลลัพธ์ไว้ใน x
        // เพื่อดูผลของการบวกค่าทศนิยม
        x = y + 5.0;
        printf("บรรทัดที่ 2: x = y + 5.0 = %.1f + 5.0 = %.1f\n", y, x);

        // คำนวณ y = x / 2.0 แสดงผลลัพธ์ของการหารด้วยทศนิยม
        y = x / 2.0;
        printf("บรรทัดที่ 3: y = x / 2.0 = %.1f / 2.0 = %.1f\n", x, y);

        // คำนวณ y ใหม่ โดยนำ x คูณกับ 3.0 แล้วบวกด้วย 4.0
        y = (x * 3.0) + 4.0;
        printf("บรรทัดที่ 4: y = (x * 3.0) + 4.0 = (%.1f * 3.0) + 4.0 = %.1f\n", x, y);

        // กำหนดค่า x ใหม่ โดยนำ -0.5 มาลบด้วย y เพื่อดูผลของค่าลบ
        x = -0.5 - y;
        printf("บรรทัดที่ 5: x = -0.5 - y = -0.5 - %.1f = %.1f\n", y, x);

        // คำนวณ z = x + y เพื่อตรวจสอบค่ารวมของตัวแปรทั้งสอง
        z = x + y;
        printf("บรรทัดที่ 6: z = x + y = %.1f + %.1f = %.1f\n", x, y, z);

        // แสดงผลลัพธ์สุดท้ายของ x, y และ z
        printf("บรรทัดที่ 7: ผลลัพธ์สุดท้าย → x = %.1f, y = %.1f, z = %.1f\n", x, y, z);

    return 0;  // คืนค่าศูนย์ให้ระบบเมื่อจบการทำงานของโปรแกรม (แสดงว่าไม่มีข้อผิดพลาด)
}
