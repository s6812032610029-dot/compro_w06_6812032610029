#include <stdio.h> // บรรทัดนี้เป็นการเรียกใช้งานไลบรารี Standard Input/Output Header (stdio.h) ซึ่งเป็นไลบรารีมาตรฐานที่จำเป็นสำหรับการใช้ฟังก์ชัน input/output อย่าง printf()

int main() { // main() คือฟังก์ชันหลักของโปรแกรม C ซึ่งเป็นจุดเริ่มต้นการทำงานของโปรแกรม
    int x = 12, y = 7, z = 12; // ประกาศตัวแปรชนิดจำนวนเต็ม (int) 3 ตัว: x, y, z พร้อมกำหนดค่าเริ่มต้นให้ x=12, y=7, และ z=12

    // 1. เปรียบเทียบ x มากกว่า y
    // x > y (12 > 7) มีค่าเป็นจริง (true) ซึ่งในภาษา C จะแสดงผลเป็น 1
    printf("1. x > y = %d\n", x > y);

    // 2. เปรียบเทียบ x น้อยกว่า z
    // x < z (12 < 12) มีค่าเป็นเท็จ (false) ซึ่งในภาษา C จะแสดงผลเป็น 0
    printf("2. x < z = %d\n", x < z);

    // 3. เปรียบเทียบ x เท่ากับ z
    // x == z (12 == 12) มีค่าเป็นจริง (true) ซึ่งในภาษา C จะแสดงผลเป็น 1
    printf("3. x == z = %d\n", x == z);

    // 4. เปรียบเทียบ x ไม่เท่ากับ y
    // x != y (12 != 7) มีค่าเป็นจริง (true) ซึ่งในภาษา C จะแสดงผลเป็น 1
    printf("4. x != y = %d\n", x != y);

    // 5. การคำนวณด้วยตัวดำเนินการตรรกะ OR (||)
    // (2*5 >= y) คือ (10 >= 7) มีค่าเป็นจริง (1)
    // (5 != (5/3)) คือ (5 != 1) มีค่าเป็นจริง (1)
    // เมื่อนำมาเชื่อมกันด้วย || (1 || 1) ผลลัพธ์ที่ได้จึงเป็นจริง (1)
    printf("5. (2*5 >= y) || (5 != (5/3)) = %d\n", (2*5 >= y) || (5 != (5/3)));

    // 6. การคำนวณด้วยตัวดำเนินการตรรกะ NOT (!)
    // (x < y) คือ (12 < 7) มีค่าเป็นเท็จ (0)
    // ! (0) ผลลัพธ์จึงกลับกันเป็นจริง (1)
    printf("6. !(x < y) = %d\n", !(x < y));

    // 7. การเปรียบเทียบค่าที่ได้จากการคำนวณ
    // (x + y) คือ (12 + 7) = 19
    // (z * 2) คือ (12 * 2) = 24
    // (19 > 24) มีค่าเป็นเท็จ (false) ซึ่งในภาษา C จะแสดงผลเป็น 0
    printf("7. ((x + y) > (z * 2)) = %d\n", ((x + y) > (z * 2)));

    // 8. การคำนวณด้วยตัวดำเนินการตรรกะ AND (&&)
    // (x % 2 == 0) คือ (12 % 2 == 0) ซึ่ง 0 == 0 มีค่าเป็นจริง (1)
    // (y % 2 == 1) คือ (7 % 2 == 1) ซึ่ง 1 == 1 มีค่าเป็นจริง (1)
    // เมื่อนำมาเชื่อมกันด้วย && (1 && 1) ผลลัพธ์ที่ได้จึงเป็นจริง (1)
    printf("8. (x %% 2 == 0) && (y %% 2 == 1) = %d\n", (x % 2 == 0) && (y % 2 == 1));

    // 9. การคำนวณด้วยตัวดำเนินการตรรกะ AND (&&)
    // (x > y) คือ (12 > 7) มีค่าเป็นจริง (1)
    // (z < y) คือ (12 < 7) มีค่าเป็นเท็จ (0)
    // เมื่อนำมาเชื่อมกันด้วย && (1 && 0) ผลลัพธ์ที่ได้จึงเป็นเท็จ (0)
    printf("9. (x > y) && (z < y) = %d\n", (x > y) && (z < y));

    return 0; // คืนค่า 0 เพื่อบอกระบบปฏิบัติการว่าโปรแกรมทำงานเสร็จสิ้นโดยไม่มีข้อผิดพลาด
}