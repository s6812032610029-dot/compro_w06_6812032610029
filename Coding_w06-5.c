#include <stdio.h>   // นำเข้าไลบรารีมาตรฐาน stdio.h ซึ่งใช้สำหรับฟังก์ชันรับค่าและแสดงผล เช่น printf()

int main() {         // ฟังก์ชันหลักของโปรแกรม เป็นจุดเริ่มต้นในการทำงานเมื่อรันโปรแกรม

    // นิพจน์นี้ใช้ตัวดำเนินการ * (คูณ) และ + (บวก) โดยเครื่องหมาย * มีลำดับความสำคัญมากกว่าบวก
    // ดังนั้นจะคำนวณ 5 * 2 ก่อน ได้ผลลัพธ์ 10 จากนั้นนำไปบวกกับ -2 จึงได้ผลลัพธ์สุดท้ายเท่ากับ 8
    int A = -2 + 5 * 2;
    printf("A = %d\n", A);   // แสดงค่า A ที่ได้จากการคำนวณ

    // นิพจน์นี้ใช้ / (หาร) และ * (คูณ) ซึ่งมีลำดับความสำคัญเท่ากัน และดำเนินการจากซ้ายไปขวา
    // เริ่มจาก 10 / 2 ได้ 5 แล้วนำ 5 ไปคูณกับ 3 ได้ผลลัพธ์สุดท้ายเป็น 15
    int B = 10 / 2 * 3;
    printf("B = %d\n", B);   // แสดงค่า B

    // นิพจน์นี้มีการใช้วงเล็บเพื่อคำนวณเศษจากการหาร 4 % 2 ซึ่งให้ค่า 0
    // แล้วนำไปคูณกับ 3 ได้ 0 จากนั้น 6 / 2 ได้ 3 แล้วนำ 3 + 0 ได้ผลลัพธ์สุดท้ายคือ 3
    int C = 6 / 2 + 3 * (4 % 2);
    printf("C = %d\n", C);   // แสดงค่า C

    // นิพจน์นี้ใช้วงเล็บเพื่อควบคุมลำดับการคำนวณ เริ่มจาก 5 + 2 ได้ 7
    // แล้วนำไปคูณกับ 15 ได้ 105 จากนั้นคำนวณ 105 % 4 ซึ่งให้เศษเท่ากับ 1
    int D = (5 + 2) * 15 % 4;
    printf("D = %d\n", D);   // แสดงค่า D

    // นิพจน์นี้ประกอบด้วยตัวดำเนินการ *, /, +, -
    // เริ่มจากคำนวณ 2 * 2 = 4 และ 6 / 2 = 3
    // จากนั้นจึงนำค่า 6 + 4 = 10 แล้วลบด้วย 3 ได้ผลลัพธ์สุดท้ายคือ 7
    int E = 6 + 2 * 2 - 6 / 2;
    printf("E = %d\n", E);   // แสดงค่า E

    // นิพจน์นี้มีตัวดำเนินการ *, /, %, +, - และวงเล็บ
    // เริ่มจาก 3 * 2 = 6, 8 / 4 = 2, และ 6 % 5 = 1
    // แล้วนำไปแทนค่าในนิพจน์: 5 + 6 - 2 + 1 = 10
    int F = 5 + 3 * 2 - 8 / 4 + (6 % 5);
    printf("F = %d\n", F);   // แสดงค่า F

    // นิพจน์นี้มีการใช้วงเล็บเพื่อควบคุมลำดับการคำนวณ
    // เริ่มจาก 4 + 3 = 7 → 7 * 2 = 14
    // อีกส่วนคือ 2 + 3 = 5 → 10 / 5 = 2
    // จากนั้นนำ 14 - 2 = 12 ซึ่งเป็นค่าผลลัพธ์สุดท้าย
    int G = (4 + 3) * 2 - 10 / (2 + 3);
    printf("G = %d\n", G);   // แสดงค่า G

    return 0;   // ส่งค่ากลับ 0 เพื่อแสดงว่าโปรแกรมทำงานเสร็จสมบูรณ์โดยไม่มีข้อผิดพลาด
}
